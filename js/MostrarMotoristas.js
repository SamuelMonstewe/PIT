function Filter() {
    document.getElementById('FilterBtn').style.transition = "0.5s"
    document.getElementById('FilterBtn').style.visibility = "visible"
    document.getElementById('FilterBtn').style.top = "0"
}

function CloseFilter() {
    document.getElementById('FilterBtn').style.transition = "0.5s"
    document.getElementById('FilterBtn').style.visibility = "hidden"
    document.getElementById('FilterBtn').style.top = "-30vh"
}
